    <h1 class="font-bold text-xl">timezone.fyi</h1>
    <p>This site lets you quickly share a time across multiple time zones.</p>
    <p>Simply type a url with the following structure:
      <br><a class="font-bold underline" href="/10:30am,pst,est">https://timezone.fyi/10:30am,pst,est</a>
    <p>The first listed time zone will be treated as the primary.
       <a class="underline" href="https://en.wikipedia.org/wiki/List_of_tz_database_time_zones">List of time zone names</a></p>

       <p>When you send these via Slack, SMS, and other modern chat clients, they'll expand to show times in every listed zone.</p>
